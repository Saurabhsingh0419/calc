public class calc {

    public static void main(String[] args) {
        double num1 = 10;
        double num2 = 5;

        // Perform operations
        double addition = num1 + num2;
        double subtraction = num1 - num2;
        double multiplication = num1 * num2;
        double division = num1 / num2;

        // Display results
        System.out.println("Addition of " + num1 + " + " + num2 + " = " + addition);
        System.out.println("Subtraction of " + num1 + " - " + num2 + " = " + subtraction);
        System.out.println("Multiplication of " + num1 + " * " + num2 + " = " + multiplication);
        System.out.println("Division of " + num1 + " / " + num2 + " = " + division);
    }
}
